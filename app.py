# 4. ФУНКЦИЯ ОПРЕДЕЛЕНИЯ ЗНАЧЕНИЯ
# ============================================================
def disambiguate(sentence, word):
    target_lemma = get_lemma(word)
    if target_lemma not in RULES:
        return None, f"Слово '{target_lemma}' отсутствует в словаре."

    cleaned_sentence = re.sub(r'[^\w\s]', ' ', sentence.lower())
    lemmatized_sentence_words = set(get_lemma(w) for w in cleaned_sentence.split() if w)
    lemmatized_sentence_words.discard(target_lemma)

    scores = {}
    for meaning, markers in RULES[target_lemma].items():
        match_count = sum(1 for marker in markers if marker in lemmatized_sentence_words)
        scores[meaning] = match_count

    if not scores:
        return None, "Нет значений для данного слова."

    best_meaning = max(scores, key=scores.get)
    best_score = scores[best_meaning]
    return best_meaning, best_score

